using System.ServiceModel.Web;
using System.Web;

namespace WcfSer4
{
    public class WcfDS3 : EntityFrameworkDataService<WcfDSUsersEntities>
    {
        // This method is called only once to initialize service-wide policies.
        public static void InitializeService(DataServiceConfiguration config)
        {
            // TODO: set rules to indicate which entity sets and service operations are visible, updatable, etc.
            // Examples:
            config.SetEntitySetAccessRule("AcceptanceSets", EntitySetRights.All);
            config.SetEntitySetAccessRule("Accounts", EntitySetRights.All);
            config.SetEntitySetPageSize("Accounts", 20);
            config.SetServiceOperationAccessRule("*", ServiceOperationRights.All);
            config.DataServiceBehavior.MaxProtocolVersion = DataServiceProtocolVersion.V3;
            config.UseVerboseErrors = true;          
        }
    }
}
 